# =========================================================
# 🐍 PYTHON NOTES: LOOPS (FOR & WHILE)
# =========================================================


# =========================================================
# 📌 1. WHAT IS A LOOP?
# =========================================================

# Loop = repeating a block of code multiple times

# Example:
# Print numbers from 1 to 5


# =========================================================
# 📌 2. FOR LOOP
# =========================================================

# Used when number of iterations is known

for i in range(1, 6):
    print(i)

# Output:
# 1
# 2
# 3
# 4
# 5


# =========================================================
# 📌 3. RANGE FUNCTION
# =========================================================

# range(start, stop, step)

for i in range(0, 10, 2):
    print(i)

# Output:
# 0
# 2
# 4
# 6
# 8


# =========================================================
# 📌 4. WHILE LOOP
# =========================================================

# Used when condition-based looping

i = 1

while i <= 5:
    print(i)
    i += 1

# Output:
# 1
# 2
# 3
# 4
# 5


# =========================================================
# 📌 5. INFINITE LOOP (IMPORTANT)
# =========================================================

# while True:
#     print("Hello")   ❌ Runs forever


# =========================================================
# 📌 6. BREAK STATEMENT
# =========================================================

# Stops loop immediately

for i in range(1, 6):
    if i == 3:
        break
    print(i)

# Output:
# 1
# 2


# =========================================================
# 📌 7. CONTINUE STATEMENT
# =========================================================

# Skips current iteration

for i in range(1, 6):
    if i == 3:
        continue
    print(i)

# Output:
# 1
# 2
# 4
# 5


# =========================================================
# 📌 8. PASS STATEMENT
# =========================================================

# Placeholder (does nothing)

for i in range(3):
    pass


# =========================================================
# 📌 9. NESTED LOOP
# =========================================================

# Loop inside another loop

for i in range(1, 4):
    for j in range(1, 3):
        print(i, j)

# Output:
# 1 1
# 1 2
# 2 1
# 2 2
# 3 1
# 3 2


# =========================================================
# 📌 10. LOOP WITH ELSE
# =========================================================

for i in range(3):
    print(i)
else:
    print("Loop finished")

# Output:
# 0
# 1
# 2
# Loop finished


# =========================================================
# 📌 11. PRACTICAL PROGRAMS
# =========================================================

# Sum of numbers

total = 0
for i in range(1, 6):
    total += i

print("Sum:", total)

# Output:
# Sum: 15


