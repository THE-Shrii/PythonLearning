# =========================================================
# 🐍 PYTHON NOTES: TUPLE
# =========================================================


# =========================================================
# 📌 1. INTRODUCTION TO TUPLE
# =========================================================

# Definition:
# Tuple is an ordered, immutable collection of elements.

# Key Features:
# ✔ Ordered (indexing allowed)
# ✔ Immutable (cannot be changed)
# ✔ Allows duplicate values
# ✔ Can store multiple data types

t = (1, 2, 3, 4)

print("Tuple:", t)
print("Type:", type(t))

# Output:
# Tuple: (1, 2, 3, 4)
# Type: <class 'tuple'>


# =========================================================
# 📌 2. SINGLE ELEMENT TUPLE (IMPORTANT)
# =========================================================

t1 = (5,)   # comma is must
print(t1)
print(type(t1))

# Output:
# (5,)
# <class 'tuple'>

# t2 = (5) ❌ int, not tuple


# =========================================================
# 📌 3. INDEXING (ORDERED PROPERTY)
# =========================================================

t = ("A", "B", "C")

print(t[0])
print(t[1])
print(t[-1])

# Output:
# A
# B
# C


# =========================================================
# 📌 4. SLICING
# =========================================================

t = (10, 20, 30, 40, 50)

print(t[1:4])

# Output:
# (20, 30, 40)


# =========================================================
# 📌 5. IMMUTABILITY
# =========================================================

t = (1, 2, 3)

# t[0] = 10 ❌ Error (cannot modify tuple)

print("Tuple is immutable")

# Output:
# Tuple is immutable


# =========================================================
# 📌 6. TUPLE METHODS
# =========================================================
# =========================================================
# 📌 1. count()
# =========================================================

# Definition:
# Returns number of times a value appears in tuple

t = (1, 2, 2, 3, 2)

print("Count of 2:", t.count(2))

# Output:
# Count of 2: 3


# =========================================================
# 📌 2. index()
# =========================================================

# Definition:
# Returns index of first occurrence of value

t = (10, 20, 30, 20)

print("Index of 20:", t.index(20))

# Output:
# Index of 20: 1


# =========================================================
# 📌 7. LOOPING IN TUPLE
# =========================================================

t = (1, 2, 3)

for i in t:
    print(i)

# Output:
# 1
# 2
# 3


# =========================================================
# 📌 8. TUPLE OPERATIONS
# =========================================================

t1 = (1, 2)
t2 = (3, 4)

# Concatenation
print(t1 + t2)

# Repetition
print(t1 * 2)

# Output:
# (1, 2, 3, 4)
# (1, 2, 1, 2)


# =========================================================
# 📌 9. MEMBERSHIP OPERATORS
# =========================================================

t = (10, 20, 30)

print(20 in t)
print(50 not in t)

# Output:
# True
# True


# =========================================================
# 📌 10. BUILT-IN FUNCTIONS
# =========================================================

t = (5, 2, 8, 1)

print(len(t))
print(max(t))
print(min(t))
print(sum(t))

# Output:
# 4
# 8
# 1
# 16


# =========================================================
# 📌 11. PACKING & UNPACKING
# =========================================================

# Packing
t = 1, 2, 3
print(t)

# Unpacking
a, b, c = (10, 20, 30)
print(a, b, c)

# Output:
# (1, 2, 3)
# 10 20 30


# =========================================================
# 📌 12. NESTED TUPLE
# =========================================================

t = (1, (2, 3), (4, 5))

print(t[1])
print(t[1][0])

# Output:
# (2, 3)
# 2


# =========================================================
# 📌 13. TYPE CONVERSION
# =========================================================

t = (1, 2, 3)

lst = list(t)
print(lst)

t2 = tuple(lst)
print(t2)

# Output:
# [1, 2, 3]
# (1, 2, 3)


# =========================================================
# 📌 14. WHY USE TUPLE?
# =========================================================

# ✔ Faster than list
# ✔ Safe (immutable)
# ✔ Used for fixed data


# =========================================================
# 📌 15. FINAL EXAM LINE
# =========================================================

print("Tuple = Ordered + Immutable + Allows Duplicates")

# Output:
# Tuple = Ordered + Immutable + Allows Duplicates