# 📌 14. X PATTERN
# =========================================================
n = 5
for i in range(n):
    for j in range(n):
        if j == i or j == n-i-1:
            print("*", end=" ")
        else:
            print(" ", end=" ")
    print()


# =========================================================
# 📌 15. PLUS (+) PATTERN
# =========================================================
n = 5
mid = n//2

for i in range(n):
    for j in range(n):
        if i == mid or j == mid:
            print("*", end=" ")
        else:
            print(" ", end=" ")
    print()


