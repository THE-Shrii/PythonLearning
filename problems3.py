# ---------------------------------------------------------
# 📌 Q2: Count vowels in a string
# ---------------------------------------------------------
s = "programming"
vowels = "aeiou"
count = 0

for ch in s:
    if ch in vowels:
        count += 1

print("Vowels:", count)
# Output: Vowels: 3


